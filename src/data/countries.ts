export const getAttractionById = (id: string): { attraction: any; country: Country } | undefined => {
  for (const country of countries) {
    const attraction = country.attractions.find(a => a.id === id);
    if (attraction) {
      return { attraction, country };
    }
  }
  return undefined;
};